package Model_Dto;

public class ContentDTO {
	int good;
	int comment_num;
	String sub_hash_tag;
	

	public String getSub_hash_tag() {
		return sub_hash_tag;
	}
	public void setSub_hash_tag(String sub_hash_tag) {
		this.sub_hash_tag = sub_hash_tag;
	}
	public int getGood() {
		return good;
	}
	public void setGood(int good) {
		this.good = good;
	}
	public int getComment_num() {
		return comment_num;
	}
	public void setComment_num(int comment_num) {
		this.comment_num = comment_num;
	}
	
	
}
